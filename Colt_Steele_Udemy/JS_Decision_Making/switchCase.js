// switch case alternative for writing conditions statements
// break keyword to stop the execution of the case else it will execute all case for a given value
const day = 4;
switch(day){
    case 1:{
        console.log("MONDAY");
        break;
    }
    case 2:{
        console.log("TUESDAY");
        break;
    }
    case 3:{
        console.log("WEDNESDAY");
        break;
    }
    case 4:{
        console.log("THURSDAY");
        break;
    }
    case 5:{
        console.log("FRIDAY");
        break;
    }
    default:{
        console.log("Either SATURDAY OR SUNDAY!")
    }
}